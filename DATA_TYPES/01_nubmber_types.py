# there are three numeric types in python
# 1. int
# 2. float
# 3. complex

# Interger(int)
num1 = 45
print(num1)
print(type(num1))

# Floating point(float)
num2 = 2.3
print(num2)
print(type(num2))

# Complex(complex)
num3 = 4 + 3j
print(num3)
print(type(num3))
print(num3.real)
print(num3.imag)
print(type(num3.real))
print(type(num3.imag))